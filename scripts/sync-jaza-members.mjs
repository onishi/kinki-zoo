import { writeFile } from "node:fs/promises";

const SOURCE_URL = "https://www.jaza.jp/search-enkan";
const OUTPUT_PATH = new URL("../src/jaza-members.ts", import.meta.url);

function decodeHtml(value) {
  return value
    .replaceAll("&amp;", "&")
    .replaceAll("&quot;", '"')
    .replaceAll("&#039;", "'")
    .replaceAll("&lt;", "<")
    .replaceAll("&gt;", ">");
}

function stripTags(value) {
  return decodeHtml(value.replace(/<br\s*\/?>/gi, " ").replace(/<[^>]+>/g, "")).trim();
}

function extractFacilities(block) {
  const facilities = [];
  const linkPattern = /<a href="([^"]+)"[^>]*>([\s\S]*?)<\/a>/g;

  for (const match of block.matchAll(linkPattern)) {
    if (/<img\b/i.test(match[2])) continue;
    const name = stripTags(match[2]);
    if (!name || facilities.some((facility) => facility.name === name)) continue;
    facilities.push({ name, website: decodeHtml(match[1]) });
  }

  return facilities;
}

const response = await fetch(SOURCE_URL, {
  headers: {
    "User-Agent": "kinki-zoo-data-sync/1.0 (+https://github.com/onishi/kinki-zoo)",
    Accept: "text/html",
  },
});
if (!response.ok) throw new Error(`JAZA fetch failed: HTTP ${response.status}`);

const html = await response.text();
const kinkiSection = html.match(/<div id="kinki"[\s\S]*?(?=<div id="china")/)?.[0];
if (!kinkiSection) throw new Error("JAZAの近畿セクションを取得できませんでした");

const zooBlock = kinkiSection.match(/<div class="zoo-park">([\s\S]*?)(?=<p class="aquarium-title">)/)?.[1];
const aquariumBlock = kinkiSection.match(/<div class="aquarium-park">([\s\S]*)/)?.[1];
if (!zooBlock || !aquariumBlock) throw new Error("JAZAの園館区分を取得できませんでした");

const zoos = extractFacilities(zooBlock);
const aquariums = extractFacilities(aquariumBlock);
if (zoos.length === 0 || aquariums.length === 0) {
  throw new Error(`JAZA施設の抽出に失敗しました: zoos=${zoos.length}, aquariums=${aquariums.length}`);
}

const output = `// Generated by scripts/sync-jaza-members.mjs. Do not edit manually.

export interface JazaMemberFacility {
  name: string;
  website: string;
}

export const JAZA_MEMBER_SOURCE_URL = ${JSON.stringify(SOURCE_URL)};

export const JAZA_KINKI_ZOOS: JazaMemberFacility[] = ${JSON.stringify(zoos, null, 2)};

export const JAZA_KINKI_AQUARIUMS: JazaMemberFacility[] = ${JSON.stringify(aquariums, null, 2)};
`;

await writeFile(OUTPUT_PATH, output, "utf8");
console.log(`JAZA近畿: 動物園 ${zoos.length}件 / 水族館 ${aquariums.length}件`);
